// Exercise: Write all possible ways to export in JavaScript modules.
// 1. Make 4 functions about addNumbers, subtractNumbers, multiplyNumbers, and divideNumbers. 
// 2. All functions need to have a return value.and at least 2 parameters.
// 3. Export those functions

export function addNumbers(num1, num2){
    return num1 + num2;
};
export function subtractNumbers(num1, num2){
    return num1 - num2;
};
export function multiplyNumbers(num1, num2){
    return num1 * num2;
};
export function divideNumbers(num1, num2){
    return num1 / num2;
};